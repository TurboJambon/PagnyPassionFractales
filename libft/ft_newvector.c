#include "libft.h"
#include <stdlib.h>

t_vector		*ft_newvector(int x, int y, int z)
{
	t_vector *vector;

	vector = malloc(sizeof(t_vector));
	if (!vector)
		return (NULL);
	vector->x = x;
	vector->y = y;
	vector->z = z;
	return (vector);
}
