#include "libft.h"

int		ft_atoi(const char *str)
{
	int x;
	int neg;
	int i;

	i = 0;
	x = 0;
	neg = 1;
	while (ft_isspace(str[i]))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while (ft_isdigit(str[i]))
	{
		x = x * 10 + str[i] - '0';
		i++;
	}
	return (x * neg);
}
