#include "fractol.h"

t_cplx	cplx_mul(t_cplx z1, t_cplx z2)
{
	t_cplx ret;

	ret.re = (z1.re * z2.re) - (z1.im * z2.im);
	ret.im = (z1.im * z2.re) + (z2.im * z1.re);
	return (ret);
}
